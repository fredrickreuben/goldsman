openSnackBar(BuildContext context, String text, SnackBarTye enumSnackBarTye, {bool isVibrate = false}) {
  if (isVibrate) {
    Vibration.vibrate(duration: 100);
  }
  showTopSnackBar(
    context,
    enumSnackBarTye == SnackBarTye.error
        ? CustomSnackBar.error(
            message: text,
          )
        : enumSnackBarTye == SnackBarTye.info
            ? CustomSnackBar.info(
                message: text,
              )
            : CustomSnackBar.success(
                message: text,
              ),
    displayDuration: const Duration(milliseconds: 500),
  );
}

enum SnackBarTye { error, info, succsess }
